void ddr_phy_util(u32 *phyreg)
{
	u32 i, len;
	u32 *data;
	const int loop_count = 1792;

	switch (starfive_ddr_type) {
	case DDR_TYPE_DDR4:
		len = sizeof(ddr4_phy_data) / sizeof(u32);
		data = ddr4_phy_data;
		break;
	case DDR_TYPE_LPDDR4:
		len = sizeof(lpddr4_phy_data) / sizeof(u32);
		data = lpddr4_phy_data;
		break;
	case DDR_TYPE_LPDDR3:
	case DDR_TYPE_DDR3:
	default:
		return;
	}

	for (i = loop_count; i < len; i++)
		out_le32(phyreg + i, data[i]);

	for (i = 0; i < loop_count; i++)
		out_le32(phyreg + i, data[i]);
}

